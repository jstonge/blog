<p>I'm currently a second-year PhD student at the <a href="https://vermontcomplexsystems.org/">Vermont Complex System Center</a>, actively involved 
    with the <a href="https://www.compethicslab.org/">Computational Ethics Lab</a> and the <a href="https://verso.w3.uvm.edu/">VERSO</a> project, the open source program office at UVM.     
    My thesis project focuses on understanding the impact of learning to code in science and how it 
    influences the research group life cycles in the midst of the computational revolution. 
    Using group-based models, I investigate the potential tension between group and individual benefits, 
    given individual relative interests or costs to learn to code. </p>

<br>

<p>I enjoy going on PhD sidequests, bringing others' works to life, my own theoretical work, 
    examining how people human biases impact the detection deepfakes in the wild, or finding 
    ways to use scientifically large language models.</p>

<br>

<p>Note that this blog is an experiment project using a combination of sveltekit and observable framework to 
    bring some of my ideas to life. In some entries, you will find the 'context' in which I wrote some applications
    that exist somewhere else in the web. I try to keep the blog the most light weight as possible,
    providing metacommentary about some of my projects that my require computation or data storage.
    In doing so, I also distribute the computational load of my stuff on different places.</p>

